ORG 0000H
MOV A,#11111110B
START:
		MOV R0,#7H
		MOV R1,#7H
ONE:	MOV	P1,A
		RL A
		LCALL DELAY
		DJNZ R0,ONE
TWO:	MOV	P1,A
		RR A
		LCALL DELAY
		DJNZ R1,TWO
		
		AJMP START
DELAY:
	MOV R3,#1
	DL0: MOV R6,#250
	DL1: MOV R7,#200
	DL2: DJNZ R7,DL2
		 DJNZ R6,DL1
		 DJNZ R3,DL0
		 RET
		 END